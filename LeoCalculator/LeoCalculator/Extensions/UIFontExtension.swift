import UIKit

extension UIFont {
    
    /// HelveticaNeue Bold 50
    static let bold50: UIFont = UIFont(name:"HelveticaNeue-Bold", size: 50.0) ?? .systemFont(ofSize: 50)
    
    /// HelveticaNeue Bold 20
    static let bold20: UIFont = UIFont(name:"HelveticaNeue-Bold", size: 20.0) ?? .systemFont(ofSize: 20)
    
    /// HelveticaNeue Bold 16
    static let bold16: UIFont = UIFont(name:"HelveticaNeue-Bold", size: 16.0) ?? .systemFont(ofSize: 16)
        
    /// HelveticaNeue Medium 14
    static let medium14: UIFont = UIFont(name:"HelveticaNeue-Medium", size: 14.0) ?? .systemFont(ofSize: 14)

}
